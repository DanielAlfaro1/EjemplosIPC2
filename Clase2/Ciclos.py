#Iteración con un ciclo while
n = 10
while n > 2 :
    print(n)
    n = n - 1
print('¡Ciclo terminado!')

#Iteración con for tipo Foreach
Compras = ['Huevos', 'Leche', 'Pan']
for elemento in Compras:
    print('Agregar al carro: ', elemento)
print('*Comprar*')

#Iteración con for
for i in range(9):
    print('Iteracion No.', i)
print('Finalizado')

